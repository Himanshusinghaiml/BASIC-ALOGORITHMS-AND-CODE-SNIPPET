class himanshu:
    def print1(self):
        print("himanshu singh ")
    def print2(self):
        print("software engineer")
        
class aiml(himanshu):
    def print3(self):
        print("hello himanshu , how r u .... this is printing from aiml class")
    def print4(self):
        print("hello aiml class")

obj=aiml()
obj.print1()
obj.print2()
obj.print3()
obj.print4()

#this is basic example of inheritance in python drvied class aiml and base class is himanshu 